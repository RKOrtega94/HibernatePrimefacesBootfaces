package model;

/**
 *
 * @author RKOrtega
 */
public class Pedido {
    private Menu menu;
    private int cantidad;

    public Pedido() {
    }

    public Pedido(Menu menu, int cantidad) {
        this.menu = menu;
        this.cantidad = cantidad;
    }

    public Menu getMenu() {
        return menu;
    }

    public void setMenu(Menu menu) {
        this.menu = menu;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

}
